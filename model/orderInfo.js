class OrderInfo  {

    constructor(id, name, phoneNumber, address, propId, custId, carItem, carTotal, status){

        this.id = id;
        this.name = name;
        this.phoneNumber = phoneNumber;
        this.address = address;
        this.propId = propId;
        this.custId = custId;
        this.carItem = carItem;
        this.carTotal = carTotal;
        this.status = status

    }

}

export default OrderInfo